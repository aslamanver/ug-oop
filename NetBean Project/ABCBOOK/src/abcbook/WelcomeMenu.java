    private void ButtonUpdateUsersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonUpdateUsersActionPerformed

        String uid = LabelUserID.getText();
        String un = TextFieldUserNameMU.getText();
        String pw = TextFieldPasswordMU.getText();
        String nm = TextFieldNameMU.getText();
        String st = TextFieldStatusMU.getText();
        String jd = TextFieldJoinDateMU.getText();
        String tl = TextFieldTelMU.getText();

        String rl;
        if (RadioButtonAdminUser.isSelected()) {
            rl = "admin";
        } else {
            rl = "user";
        }

        if (un.isEmpty() || st.isEmpty() || jd.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Type username,join date and status", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            if (this.books.updateUsers(un, pw, nm, st, jd, tl, rl, uid)) {
                //JOptionPane.showMessageDialog(this, "Successfully Updated ", "Success", JOptionPane.INFORMATION_MESSAGE);
                this.notificationCon.notificationMessage("Success - " + nm, "Successfully Updated ", "info");
            } else {
                JOptionPane.showMessageDialog(this, "This record has not been successfully updated ", "Error", JOptionPane.WARNING_MESSAGE);

            }
            usersTable();
        }
    }//GEN-LAST:event_ButtonUpdateUsersActionPerformed


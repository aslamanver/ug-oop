    public void searchDetails() {

        TabbedPaneWelcome.setSelectedIndex(0);

        clearTotalResultsTable();

        DefaultTableModel mytbl = (DefaultTableModel) TableResults.getModel();

        String searchTerm = "id";
        if (this.RadioButtonName.isSelected()) {
            searchTerm = "name";
        } else if (this.RadioButtonAuthor.isSelected()) {
            searchTerm = "author";
        } else {
            searchTerm = "id";
        }

        if (ComboBoxFilterSearch.getSelectedItem().toString() == "All Categories") {

            books.setQuery("SELECT * FROM books WHERE " + searchTerm + " LIKE '%" + TextFieldSearch.getText() + "%' ORDER BY " + ComboBoxSortFront.getSelectedItem().toString() + " ASC");

        } else {

            books.setQuery("SELECT * FROM books WHERE " + searchTerm + " LIKE '%" + TextFieldSearch.getText() + "%' AND category = '" + ComboBoxFilterSearch.getSelectedItem().toString() + "' ORDER BY " + ComboBoxSortFront.getSelectedItem().toString() + " ASC");
        }

        try {

            books.rs = books.st.executeQuery(books.sql);

            while (books.rs.next()) {
                //Add Row
                mytbl.addRow(new Object[]{
                    books.rs.getString("id"),
                    books.rs.getString("name"),
                    books.rs.getString("author"),
                    books.rs.getString("category"),
                    books.rs.getString("stock"),
                    books.rs.getString("date"),
                    books.rs.getString("addedby"),
                    books.rs.getString("barcode")});
            }
            LabelResultsFront.setText("Total Results - " + TableResults.getRowCount());

        } catch (SQLException ex) {
            System.out.println("Error 2 " + ex.getMessage());
        }

        if (TableResults.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No results found for your search term please retry another", "Results", JOptionPane.INFORMATION_MESSAGE);
        }
    }


    private void ButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonInsertActionPerformed

        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        Date date = new Date();
        String nowdate = dateFormat.format(date);

        if ((isInteger(this.TextFieldStockBookInsert.getText())) && (isInteger(this.TextFieldBarcodeBookInsert.getText())) && (!this.TextFieldNameBookInsert.getText().isEmpty())) {

            if (this.books.insertBook(this.TextFieldNameBookInsert.getText(), this.TextFieldAuthorBookInsert.getText(), this.TextFieldCategoryBookInsert.getText(), Integer.parseInt(this.TextFieldStockBookInsert.getText()), nowdate, this.books.getCurrentUser(), this.TextFieldBarcodeBookInsert.getText())) {
                //JOptionPane.showMessageDialog(this, "Your book has been inserted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                this.notificationCon.notificationMessage("Inserted", this.TextFieldNameBookInsert.getText() + " The book has been successfully inserted", "info");
                clearInsertBooks();
                this.booksTable();

            } else {
                JOptionPane.showMessageDialog(this, "Error with your data please check barcode and details it and try again", "Error", JOptionPane.WARNING_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Please type stock and barcode in number also name is optional !", "Number", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ButtonInsertActionPerformed


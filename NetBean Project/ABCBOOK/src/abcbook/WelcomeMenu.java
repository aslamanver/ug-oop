    private void ComboBoxUsersSortItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ComboBoxUsersSortItemStateChanged

        //Table - Users
        DefaultTableModel mytblu = (DefaultTableModel) TableUsers.getModel();

        //Remove rows default rows
        int rowCount = mytblu.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            mytblu.removeRow(i);
        }

        books.usersOrderBy(ComboBoxUsersSort.getSelectedItem().toString());
        try {

            books.rs = books.st.executeQuery(books.sql);

            while (books.rs.next()) {

                //Add Row
                mytblu.addRow(new Object[]{
                    books.rs.getString("id"),
                    books.rs.getString("name"),
                    books.rs.getString("username"),
                    books.rs.getString("password"),
                    books.rs.getString("status"),
                    books.rs.getString("joindate"),
                    books.rs.getString("tel"),
                    books.rs.getString("role")
                });
            }
            LabelUsersResults.setText("Total Results - " + TableUsers.getRowCount());

        } catch (SQLException ex) {
            System.out.println("Error from Front " + ex.getMessage());
        }
    }//GEN-LAST:event_ComboBoxUsersSortItemStateChanged

